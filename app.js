const calculator = {
  add: function (a, b) {
    console.log(a + b);
  },
  minus: function (a, b) {
    console.log(a - b);
  },
  devide: function (a, b) {
    console.log(a / b);
  },
  squared: function (a, b) {
    console.log(a ** b);
  },
};

calculator.add(7, 6);
calculator.minus(10, 4);
calculator.devide(8, 2);
calculator.squared(5, 3);
